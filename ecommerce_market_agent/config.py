from datetime import date, timedelta

COLLECT_DAYS = 365

def get_date_range(days: int = COLLECT_DAYS) -> tuple[str, str]:
    end   = date.today()
    start = end - timedelta(days=days)
    return start.strftime("%Y-%m-%d"), end.strftime("%Y-%m-%d")

